     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|max:100',
        ]);
        $user = new User();
        $user->name = $request->get('name');
        $user->save();

        return redirect('users')->with('Запись добавлена');
    }

